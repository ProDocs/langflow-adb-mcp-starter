CREATE TABLE LANGFLOW.CUSTOMERS 
    ( 
     ID     NUMBER GENERATED ALWAYS AS IDENTITY 
        ( START WITH 1 CACHE 20 )  NOT NULL , 
     NOME   VARCHAR2 (100) , 
     CIDADE VARCHAR2 (60) 
    ) 
;


CREATE TABLE LANGFLOW.PEDIDO 
    ( 
     ID          NUMBER GENERATED ALWAYS AS IDENTITY 
        ( START WITH 1 CACHE 20 )  NOT NULL , 
     CLIENTE_ID  NUMBER  NOT NULL , 
     VALOR       NUMBER (10,2)  NOT NULL , 
     DATA_PEDIDO DATE DEFAULT SYSDATE 
    ) 
;


CREATE TABLE LANGFLOW.RAG_CHUNKS 
    ( 
     ID         NUMBER GENERATED BY DEFAULT AS IDENTITY 
        ( START WITH 1 CACHE 20 ) , 
     DOC_ID     VARCHAR2 (200) , 
     CHUNK_ID   NUMBER , 
     CHUNK_TEXT CLOB , 
     EMBEDDING  VECTOR(*,*,DENSE) , 
     META_JSON  CLOB , 
     CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP 
    ) 
;

ALTER TABLE LANGFLOW.RAG_CHUNKS 
    ADD PRIMARY KEY ( ID ) 
